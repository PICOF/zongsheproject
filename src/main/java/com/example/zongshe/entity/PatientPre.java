package com.example.zongshe.entity;

public class PatientPre {
    private int pbid;
    private String tpre;
    private String ypre;
    private String active;

    public int getPbid() {
        return pbid;
    }

    public void setPbid(int pbid) {
        this.pbid = pbid;
    }

    public String getTpre() {
        return tpre;
    }

    public void setTpre(String tpre) {
        this.tpre = tpre;
    }

    public String getYpre() {
        return ypre;
    }

    public void setYpre(String ypre) {
        this.ypre = ypre;
    }

    public String getActive() {
        return active;
    }

    public void setActive(String active) {
        this.active = active;
    }
}
